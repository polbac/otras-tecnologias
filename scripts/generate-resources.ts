type ResourceSeed = {
  slug: string;
  title: string;
  description: string;
  authors: string[];
  link: string;
  createdAt: string; // YYYY-MM-DD
  tags: string[];
  image?: string;
};

const OUT_DIR = path.join(process.cwd(), "src", "content", "resources");

function fmEscape(value: string) {
  // safe single-line YAML string
  const v = value.replace(/\\/g, "\\\\").replace(/"/g, '\\"');
  return `"${v}"`;
}

function toFrontmatter(r: ResourceSeed) {
  const lines: string[] = [];
  lines.push("---");
  lines.push(`title: ${fmEscape(r.title)}`);
  lines.push(`description: ${fmEscape(r.description)}`);
  lines.push(`authors: [${r.authors.map(fmEscape).join(", ")}]`);
  lines.push(`link: ${fmEscape(r.link)}`);
  lines.push(`createdAt: ${fmEscape(r.createdAt)}`);
  if (r.image) lines.push(`image: ${fmEscape(r.image)}`);
  lines.push(`tags: [${r.tags.map(fmEscape).join(", ")}]`);
  lines.push("---");
  return lines.join("\n");
}

