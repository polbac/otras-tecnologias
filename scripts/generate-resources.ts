async function main() {
  await fs.mkdir(OUT_DIR, { recursive: true });

  const existing = new Set(await fs.readdir(OUT_DIR).catch(() => []));
  const filesToWrite: Array<{ filename: string; content: string }> = [];

  for (const r of SEED) {
    const filename = `${r.slug}.md`;
    const full = path.join(OUT_DIR, filename);
    const fm = toFrontmatter(r);
    const body = [
      fm,
      "",
      `# ${r.title}`,
      "",
      r.description,
      "",
      "",
    ].join("\n");

    // do not overwrite user edits unless file is missing
    if (existing.has(filename)) continue;
    filesToWrite.push({ filename: full, content: body });
  }

  await Promise.all(filesToWrite.map((f) => fs.writeFile(f.filename, f.content, "utf8")));

  // small index file so the folder exists even if seed changes
  const metaPath = path.join(OUT_DIR, "_seed.json");
  await fs.writeFile(
    metaPath,
    JSON.stringify({ generatedAt: new Date().toISOString(), count: SEED.length }, null, 2) + "\n",
    "utf8",
  );

  const written = filesToWrite.length;
  console.log(`OK: resources dir: ${OUT_DIR}`);
  console.log(`OK: seed entries: ${SEED.length}`);
  console.log(`OK: wrote new files: ${written}`);
  if (written === 0) console.log("Nota: no se sobreescribio nada (ya existian los archivos).");
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});

